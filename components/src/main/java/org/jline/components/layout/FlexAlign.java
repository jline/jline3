package org.jline.components.layout;

/**
 * Cross-axis alignment for flexbox layout.
 */
public enum FlexAlign {
    /** Align to start of cross axis. */
    START,
    /** Center on cross axis. */
    CENTER,
    /** Align to end of cross axis. */
    END,
    /** Stretch to fill cross axis. */
    STRETCH
}
